"""
Planning Agent — sinh 3 "golden brief" cho buổi phỏng vấn (thiết kế tách-brief).

Vì sao tách brief thay vì xuất 1 JSON chứa cả 3 brief?
  Gemma gateway (VNG MaaS) rất hay CẮT/treo khi phải trả một JSON DÀI chứa 3 khối
  markdown (escape \\n nhiều, dễ hỏng → "Invalid JSON: EOF"). Đó là nguồn lỗi
  định dạng + timeout chính. Brief vốn LÀ markdown thuần, không cần bọc JSON.

Thiết kế ổn định:
  1. Grounding chạy TRỰC TIẾP (deterministic, không qua LLM): extract_requirements
     → match_skills → search_problem_bank. Rẻ, không tốn round-trip.
  2. Sinh 3 brief bằng 3 lời gọi LLM RIÊNG, mỗi lời trả MARKDOWN THUẦN (không
     response_format, không JSON). Output mỗi lời ngắn → không bị cắt.
  3. Ba lời gọi chạy TUẦN TỰ (từng brief một). Gateway MaaS chỉ phục vụ ~2
     request đồng thời/key — bắn cả 3 song song khiến brief thứ 3 bị bỏ đói
     (~34s) và dễ chạm timeout → plan degraded; tệ hơn khi nhiều link được tạo
     cùng lúc trong production. Chạy nối tiếp đánh đổi một ít wall-clock danh
     nghĩa lấy sự ỔN ĐỊNH: không starvation, không quá tải gateway, không timeout
     dây chuyền.

Mỗi brief được inject thẳng vào prompt của agent hạ nguồn:
  - interview_brief  → Interview Agent (dẫn dắt hội thoại)
  - evaluation_brief → Inspector Agent (chấm điểm)
  - assignment_brief → Code Assignment Agent (CODING or COGNITIVE test)

assignment_brief is a GENERIC brief: it does not produce the concrete task. It
opens with a one-line, machine-readable ASSIGNMENT DIRECTIVE (in English) telling
the Assignment Agent which kind of task to build (coding|cognitive), the mode
(dsa|project) and whether the AI assistant is enabled. The concrete problem /
questions are generated by the Assignment Agent only when the candidate joins.
"""

