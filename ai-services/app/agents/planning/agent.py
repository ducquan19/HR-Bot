# The 3 briefs now run SEQUENTIALLY within a single request (see run_planning_agent),
# so this semaphore no longer bounds in-request concurrency — it bounds CROSS-request
# concurrency: when several links are created at once, it caps how many planning LLM
# calls hit the gateway together. The MaaS gateway was measured to serve only ~2
# concurrent requests per key (a 3rd is STARVED to ~34s), so keep this at 2 to match
# the gateway's real capacity and never oversubscribe it. Module-level so all
# simultaneous link creations share the cap.
_LLM_CONCURRENCY = asyncio.Semaphore(2)


